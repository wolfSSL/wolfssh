    wolfSSH_set_fd(clientSsh, (int)sockFd);

    ret = wolfSSH_connect(clientSsh);
    AssertIntEQ(ret, WS_FATAL_ERROR);

    wolfSSH_shutdown(clientSsh);
    WCLOSESOCKET(sockFd);
